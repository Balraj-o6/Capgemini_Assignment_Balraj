package com.cg.collection;

public class Employee3 {
	private int empId;
	private String empName;
	private double salary;

	public Employee3() {
	}

	public Employee3(int empId, String empName, double salary) {
		this.empId = empId;
		this.empName = empName;
		this.salary = salary;
	}

	public int getEmpId() {
		return empId;
	}

	public String getEmpName() {
		return empName;
	}

	public double getSalary() {
		return salary;
	}
}
